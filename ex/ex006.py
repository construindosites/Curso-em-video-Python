# Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada 
n = int(input('Digite um número: '))
dobro = n * 2
triplo = n * 3 
raiz = n ** (1/2)
print('O numero digitado foi {}.\n O seu dobro é {} \n o seu triplo é {} \n e sua raiz quadrada é {}'.format((n), (dobro), (triplo), (raiz)))
